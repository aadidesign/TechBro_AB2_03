export const getActivityColor = (type) => {
  switch (type.toLowerCase()) {
    case 'appointment':
      return 'text-blue-400 bg-blue-400/10';
    case 'prescription':
      return 'text-purple-400 bg-purple-400/10';
    default:
      return 'text-gray-400 bg-gray-400/10';
  }
}; 